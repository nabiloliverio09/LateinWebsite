// Hilfsfunktion: Gibt eine zufällige Frage basierend auf der Aktion zurück
export function getQuestionForAction(action, answeredIds = []) {
  let categories = [];
  if (action === 'gather') {
    categories = ['Geschichte', 'Militär'];
  } else if (action === 'recruit') {
    categories = ['Übersetzung', 'Vokabeln'];
  } else if (action === 'attack') {
    categories = ['Caesar'];
  }

  const filtered = quizQuestions.filter(q => categories.includes(q.category));
  
  // Filter out correctly answered questions
  let unanswered = filtered.filter(q => !answeredIds.includes(q.id));
  
  // Reset pool if all questions in these categories have been answered correctly once
  if (unanswered.length === 0) {
    unanswered = filtered;
  }
  
  const randomIndex = Math.floor(Math.random() * unanswered.length);
  return unanswered[randomIndex];
}
