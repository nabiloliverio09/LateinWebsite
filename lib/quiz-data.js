// Hilfsfunktion: Gibt eine zufällige Frage basierend auf der Aktion zurück
export function getQuestionForAction(action) {
  let categories = [];
  if (action === 'gather') {
    categories = ['Geschichte', 'Militär'];
  } else if (action === 'recruit') {
    categories = ['Übersetzung', 'Vokabeln'];
  } else if (action === 'attack') {
    categories = ['Caesar'];
  }

  const filtered = quizQuestions.filter(q => categories.includes(q.category));
  if (filtered.length === 0) return quizQuestions[0]; // Fallback
  
  const randomIndex = Math.floor(Math.random() * filtered.length);
  return filtered[randomIndex];
}
